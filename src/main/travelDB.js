export const getCommentRank = (spot='국립중앙박물관') => {
    console.log('getCommentRank()');

    let commentDB = JSON.parse(localStorage.getItem('commentDB'));

    let strRank = '';

    if (commentDB[spot] === undefined) {

        strRank = '☆☆☆☆☆';

    } else {

        let sumRank = 0;
        for (let i = 0; i < commentDB[spot].length; i++)
            sumRank += Number(commentDB[spot][i].rank)

        let rank = Math.floor(sumRank/commentDB[spot].length)

        rank === 0 ? rank = 1 : rank=rank;

        for (let i = 0; i < rank; i++)
            strRank += '★';
        
        for (let i = 0; i< 5-rank; i++)
            strRank += '☆';
    }
    return strRank;

}


